const PIXEL = 7; // CSS px per pixel

export function DigimonSprite({ stage, isAnimating, isSick, isSleeping }: SpriteProps) {
  const grid = SPRITES[stage] ?? SPRITES.egg;
  const cols = grid[0].length;
  const rows = grid.length;
  const width = cols * PIXEL;
  const height = rows * PIXEL;

  const filter = isSick
    ? "hue-rotate(90deg) saturate(1.5)"
    : isSleeping
    ? "brightness(0.5) saturate(0.3)"
    : "none";

  return (
    <div
      style={{
        width,
        height,
        position: "relative",
        imageRendering: "pixelated",
        filter,
        animation: isAnimating ? "digiFloat 0.6s ease-in-out infinite alternate" : "none",
      }}
    >
      <svg
        width={width}
        height={height}
        viewBox={`0 0 ${width} ${height}`}
        style={{ imageRendering: "pixelated", display: "block" }}
      >
        {grid.flatMap((row, r) =>
          row.map((key, c) => {
            const color = C[key as keyof typeof C];
            if (!color || color === "transparent") return null;
            return (
              <rect
                key={`${r}-${c}`}
                x={c * PIXEL}
                y={r * PIXEL}
                width={PIXEL}
                height={PIXEL}
                fill={color}
              />
            );
          })
        )}
      </svg>

      {isSleeping && (
        <div
          style={{
            position: "absolute",
            top: 0,
            right: -24,
            fontSize: 14,
            animation: "sleepBubble 2s ease-in-out infinite",
            fontFamily: "monospace",
            color: "#aaccff",
          }}
        >
          z z z
        </div>
      )}
    </div>
  );
}
