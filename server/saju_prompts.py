def build_intro(manse_json: dict) -> str:
    """풀이 결과 앞에 자동으로 붙이는 도입부 — 사주 표 + 중심 기운 + 분포."""
    fp = manse_json.get("four_pillars", {})
    day = fp.get("day", {})
    day_gan_ko = day.get("gan_ko", "?")
    fe = manse_json.get("five_elements_count", {})
    ko_map = [("wood", "나무"), ("fire", "불"), ("earth", "흙"),
              ("metal", "쇠"), ("water", "물")]
    five_desc = " · ".join(
        f"**{ko} {fe.get(code, 0)}**" for code, ko in ko_map
    )

    gender_ko = "남성" if manse_json.get("input_echo", {}).get("gender") == "male" else "여성"
    cal_ko = "양력" if manse_json.get("input_echo", {}).get("calendar") == "solar" else "음력"
    ymd = manse_json.get("input_echo", {}).get("ymdhm", [])

    GAN_BIYU = {
        "갑": "우뚝 선 큰 나무", "을": "부드러운 풀·덩굴",
        "병": "한낮의 태양", "정": "따뜻한 촛불·등불",
        "무": "든든한 큰 산", "기": "비옥한 들판",
        "경": "거친 원석·도끼", "신": "곱게 다듬어진 보석·칼날",
        "임": "넓은 바다·강물", "계": "잔잔한 시냇물",
    }
    biyu = GAN_BIYU.get(day_gan_ko, "고유한 기운")

    def cell(key: str) -> tuple[str, str]:
        p = fp.get(key)
        if not p:
            return ("?", "?")
        gan = p.get("gan_ko", "?")
        if key == "day":
            gan = f"**{gan}**"
        return (gan, p.get("ji_ko", "?"))

    y_g, y_j = cell("year"); m_g, m_j = cell("month")
    d_g, d_j = cell("day"); h_g, h_j = cell("hour")

    return (
        f"**사주 여덟 글자** ({cal_ko} {ymd[0]}년 {ymd[1]}월 {ymd[2]}일 {ymd[3]}시, {gender_ko})\n\n"
        f"태어난 해 — 위 글자 **{y_g}**, 아래 글자 **{y_j}**\n"
        f"태어난 달 — 위 글자 **{m_g}**, 아래 글자 **{m_j}**\n"
        f"태어난 날 — 위 글자 {d_g}, 아래 글자 **{d_j}**  ← 중심이 되는 자리\n"
        f"태어난 시각 — 위 글자 **{h_g}**, 아래 글자 **{h_j}**\n\n"
        f"타고난 중심 기운은 **{biyu}** 같은 기운이에요.\n"
        f"기운 분포 — {five_desc}\n\n"
    )
